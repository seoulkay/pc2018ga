package tis.pye.team.vo;

public class TisFacilities {
	private int id;
	private String fac_title;
	private String fac_title_loc;
	private String fac_icon;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFac_title() {
		return fac_title;
	}
	public void setFac_title(String fac_title) {
		this.fac_title = fac_title;
	}
	public String getFac_title_loc() {
		return fac_title_loc;
	}
	public void setFac_title_loc(String fac_title_loc) {
		this.fac_title_loc = fac_title_loc;
	}
	public String getFac_icon() {
		return fac_icon;
	}
	public void setFac_icon(String fac_icon) {
		this.fac_icon = fac_icon;
	}
	
}
