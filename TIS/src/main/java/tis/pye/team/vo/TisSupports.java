package tis.pye.team.vo;

public class TisSupports {
	private int id;
	private int event_id;
	private String support_title;
	private String support_tel;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getEvent_id() {
		return event_id;
	}
	public void setEvent_id(int event_id) {
		this.event_id = event_id;
	}
	public String getSupport_title() {
		return support_title;
	}
	public void setSupport_title(String support_title) {
		this.support_title = support_title;
	}
	public String getSupport_tel() {
		return support_tel;
	}
	public void setSupport_tel(String support_tel) {
		this.support_tel = support_tel;
	}
	
	
}
