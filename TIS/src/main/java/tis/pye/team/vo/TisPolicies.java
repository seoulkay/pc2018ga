public class TisPolicies {
	private int id;
	private String pol_title;
	private String pol_title_loc;
	private String pol_icon;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPol_title() {
		return pol_title;
	}
	public void setPol_title(String pol_title) {
		this.pol_title = pol_title;
	}
	public String getPol_title_loc() {
		return pol_title_loc;
	}
	public void setPol_title_loc(String pol_title_loc) {
		this.pol_title_loc = pol_title_loc;
	}
	public String getPol_icon() {
		return pol_icon;
	}
	public void setPol_icon(String pol_icon) {
		this.pol_icon = pol_icon;
	}
	
	
}
