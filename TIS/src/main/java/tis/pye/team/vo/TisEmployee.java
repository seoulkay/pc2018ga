package tis.pye.team.vo;

public class TisEmployee {
	private int id;
	private int event_id;
	private String picture;
	private String first_name;
	private String last_name;
	private String employee_group;
	private String tel;
	private String email;
	private String job_title;
	private String atos_id;
	private String pin;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getEvent_id() {
		return event_id;
	}
	public void setEvent_id(int event_id) {
		this.event_id = event_id;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public String getFirst_name() {
		return first_name;
	}
	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}
	public String getLast_name() {
		return last_name;
	}
	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}
	
	public String getEmployee_group() {
		return employee_group;
	}
	public void setEmployee_group(String employee_group) {
		this.employee_group = employee_group;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getJob_title() {
		return job_title;
	}
	public void setJob_title(String job_title) {
		this.job_title = job_title;
	}
	public String getAtos_id() {
		return atos_id;
	}
	public void setAtos_id(String atos_id) {
		this.atos_id = atos_id;
	}
	public String getPin() {
		return pin;
	}
	public void setPin(String pin) {
		this.pin = pin;
	}
}
